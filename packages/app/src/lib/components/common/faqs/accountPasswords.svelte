<p>Plebeian market uses the Nostr protocol for account login and verification.</p>
<p>
	If you already use Nostr then you can login with your existing profile or if you want to separate your selling profiles from your personal
	one you can create a new one. Just remember to write down your private keys and keep them safe. Use an extension or N-Connect for the best
	security when authenticating with the site. You will not be able to regain access to your account if you lose it.
</p>
<p>
	If you generate an account you will be asked to provide a password, this is so that we can authenticate you are the correct user when
	resuming activity in the browser
</p>
